<script type="text/ecmascript-6">
  import {formatDate} from '../../assets/js/date.js';

  export default {
    name:"Study",
    data () {
      return {
        options1:[
          {value:"计算机系"},
          {value:"外语系"},
          {value:"艺术系"},
          {value:"热作系系"},
        ],
        options2:[
          {value:"博士"},
          {value:"硕士"},
          {value:"学士"},
          {value:"其他"},
        ],
        time:[],
        DataLists:[],
        viewData:{
          Id:'',
          IsHome:'',
          BeginDate:'',
          EndDate:'',
          College:'',
          Major:'',
          Degree:'',
        },
      };
    },
    filters: {
      formatDate: function (time) { //时间戳转日期
        let date = new Date(time * 1000);
        return formatDate(date, 'yyyy-MM-dd');
      },
      ifHome: function (value) {
        return value==1? "国内":"国外";
      }
    },
    mounted: function () {
      this.getNewData();
    },
    methods:{
      //获取数据列表
      getNewData: function () {
        let vm = this;
        let userid = sessionStorage.getItem("userId");
        if(!userid || userid == 0) return false;
        vm.$axios({
            method:'post',
            url:vm.$api + '/educations?userid=' + userid,
        })
           .then(function(res){
             var res = res.data;
             if(res.code == 0){
               vm.DataLists = res.result;
             }else {
               vm.$message.error(res.message);
             }
           })
           .catch(function(err){
             alert(err);
           });
      },
      //保存并新增学习经历
      addNewStudy: function () {
        let vm = this;
        var userid = sessionStorage.getItem("userId");
        if(!userid || userid ==0) {
          vm.$message.warning("请先填写人才信息并保存或到人才列表选择单个人才查看!");
          return false;
        }
        let data = JSON.parse(JSON.stringify(vm.viewData));
        data.BeginDate = vm.time[0]/1000;
        data.EndDate = vm.time[1]/1000;
        data.Id = "0";
        data.UserId = userid;
        vm.$axios({
            method:'post',
            url:vm.$api + '/seteducation?operate=1&id=0',
            data:JSON.stringify(data)
        })
           .then(function(res){
             var res = res.data;
             if(res.code == 0){
               vm.$message.success("新增并保存学习经历成功!");
               vm.getNewData();
             }else {
                vm.$message.error(res.message);
             }
           })
           .catch(function(err){
             alert(err);
           });
      },
      //保存修改学习经历
      saveStudy: function () {
        let vm = this;
        var userid = sessionStorage.getItem("userId");
        if (!userid || userid == 0) {
          vm.$message.warning("请先填写人才信息并保存或到人才列表选择单个人才查看!");
          return false;
        };
        let data = JSON.parse(JSON.stringify(vm.viewData));
        data.BeginDate = vm.time[0]/1000;
        data.EndDate = vm.time[1]/1000;
        vm.$axios({
          method:'post',
          url:vm.$api + '/seteducation?operate=2&id='+ data.Id,
          data:JSON.stringify(data)
        })
          .then(function(res){
            var res = res.data;
            if(res.code == 0){
              vm.$message.success("修改并保存学习经历成功!");
              vm.getNewData();
            }else {
              vm.$message.error(res.message);
            }
          })
          .catch(function(err){
            alert(err);
          });
      },
      //获取单项详情
      getInfo: function (id) {
        let vm = this;
        console.log(id);
        vm.$axios({
            method:'post',
            url:vm.$api + '/education?id=' + id,
        })
           .then(function(res){
             var res = res.data;
             if(res.code == 0){
               vm.viewData = res.result;
               vm.viewData.Id = id;
               vm.time = [vm.viewData.BeginDate*1000,vm.viewData.EndDate*1000];
             }else {
               vm.$message.error(res.message);
             }
           })
           .catch(function(err){
             alert(err);
           });
      },
      //删除学习经历
      delStudy: function (id) {
        let vm = this;
        vm.$axios({
          method:'post',
          url:vm.$api+'/deleteeducation?id='+id,
        })
          .then(function(res){
            var res = res.data;
            if(res.code == 0 ){
              vm.$message.success('删除成功!');
              vm.getNewData();
            }else {
              vm.$message.error(res.message);
            }
          })
          .catch(function(err){
            alert(err);
          });
      }
    }
  }
</script>

